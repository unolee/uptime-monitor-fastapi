def get_benchmark_sites(count: int) -> list[dict]:
    """
    Return a list of benchmark site dicts, up to `count` items.
    Each dict has 'name' and 'url' keys.
    """
    # Deduplicate while preserving order
    seen = set()
    unique_sites = []
    for url in BENCHMARK_SITES:
        if url not in seen:
            seen.add(url)
            unique_sites.append(url)

    selected = unique_sites[:count]
    results = []
    for url in selected:
        # Derive a name from the URL
        name = url.replace("https://", "").replace("http://", "").rstrip("/")
        results.append({"name": name, "url": url})
    return results
